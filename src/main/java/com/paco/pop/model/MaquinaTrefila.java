package com.paco.pop.model;

public class MaquinaTrefila {

	private int MaquinaTrefila;
	private String maquina;
	private boolean isQuebrada;
	
	public int getMaquinaTrefila() {
		return MaquinaTrefila;
	}
	public void setMaquinaTrefila(int maquinaTrefila) {
		MaquinaTrefila = maquinaTrefila;
	}
	public String getMaquina() {
		return maquina;
	}
	public void setMaquina(String maquina) {
		this.maquina = maquina;
	}
	public boolean isQuebrada() {
		return isQuebrada;
	}
	public void setQuebrada(boolean isQuebrada) {
		this.isQuebrada = isQuebrada;
	}



	
}
